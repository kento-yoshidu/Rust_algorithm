pub fn run(vec: Vec<usize>) -> usize {
    let mut ans = 0;

    for (i, num) in vec.iter().enumerate() {
        ans += num << i;
    }

    ans
}

fn run2(vec: Vec<usize>) -> usize {
    vec.iter()
        .enumerate()
        .fold(0, |sum, (i, v)| { sum + (v << i) })
}

