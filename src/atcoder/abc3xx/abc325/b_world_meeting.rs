fn run(_n: usize, vec: Vec<(usize, usize)>) -> usize {
    let mut ans = 0;

    for i in 0..24 {
        let mut count = 0;

        for (w, x) in vec.iter() {
            // 現地時間
            let time = (i+x)%24;

            if 9 <= time && time <= 17 {
                count += w;
            }
        }

        ans = ans.max(count)
    }

    ans
}

