fn run(h: usize, w: usize, s: Vec<&str>) -> isize {
    let vec: Vec<Vec<char>> = s.iter().map(|s| s.chars().collect()).collect();

    let mut point: Vec<(isize, isize)> = Vec::new();

    for i in 0..h {
        for j in 0..w {
            if vec[i][j] == 'o' {
                point.push((i as isize, j as isize));
            }
        }
    }

    (point[0].0 - point[1].0).abs() + (point[0].1 - point[1].1).abs()
}

