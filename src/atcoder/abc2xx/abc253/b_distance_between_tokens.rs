// https://atcoder.jp/contests/abc253/tasks/abc253_b

pub fn run(h: usize, w: usize, s: Vec<&str>) -> i32 {
    let vec: Vec<Vec<char>> = s.iter().map(|s| s.chars().collect()).collect();

    let mut point: Vec<(i32, i32)> = Vec::new();

    for i in 0..h {
        for j in 0..w {
            if vec[i][j] == 'o' {
                point.push((i as i32, j as i32));
            }
        }
    }

    (point[0].0 - point[1].0).abs() + (point[0].1 - point[1].1).abs()
}

